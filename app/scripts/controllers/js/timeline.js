function initTimeline($scope) {
    setInterval(function() {
        var ichecks = $('input[type="checkbox"].flat-red, input[type="radio"].flat-red');
        ichecks.iCheck({
            checkboxClass: 'icheckbox_flat-red',
            radioClass: 'iradio_flat-red',
            disabledClass: 'non-clickable'
        });
    }, 300);
}